/*Realizar un algoritmo en JavaScript que permita calcular la suma, resta, multiplicación y división de dos números,
 donde num1 = 5 y num2 = 7. Mostrar los resultados por consola.*/

 let num1 = 5;
 let num2 = 7;
 let sum=0,res=0,multi=0,div=0;

 sum = num1+num2;
 res = num1-num2;
 multi = num1*num2;
 div = num1/num2;

 alert (`Operaciones basicas con los numeros ${num1} y ${num2}: \n 
 Suma: ${sum} \n 
 Resta: ${res} \n 
 Multipliacion: ${multi} \n 
 Division: ${div}`);

